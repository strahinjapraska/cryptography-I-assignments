from gmpy2 import mpz,powmod,divm 

B = 2**20 

def build_table(p,g,h):
    table = {}

    for i in range(B+1):
        l = divm(h,powmod(g,i,p),p)
        table.update({l:i}) 

    return table 

def find_in_table(table,g):
    g_B = powmod(g,B,p)
    for i in range(B+1):

        r = powmod(g_B,i,p)
        x1 = table.get(r)

        if x1 is not None:  
            return mpz(i)*B+mpz(x1)


